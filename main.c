#include "libft.h"
#include <stdio.h>
#include <string.h>
#include <ctype.h>

int main () {
   int var1 = '2';
   int var2 = '5';
    
   if( ft_isdigit(var1) ) {
      printf("var1 = |%c| is a digit\n", var1 );
   } else {
      printf("var1 = |%c| is not a digit\n", var1 );
   }
   
   if( ft_isdigit(var2) ) {
      printf("var2 = |%c| is a digit\n", var2 );
   } else {
      printf("var2 = |%c| is not a digit\n", var2 );
   }
   
   return(0);
}
